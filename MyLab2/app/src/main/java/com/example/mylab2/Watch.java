package com.example.mylab2;

public class Watch {
    private String make;
    private String model;
    private String power_source;
    private Integer water_resistance;
    private String band_content;
    private Double price;

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }



    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }


    public String getPower_source() {
        return power_source;
    }

    public void setPower_source(String power_source) {
        this.power_source = power_source;
    }



    public Integer getWater_resistance() {
        return water_resistance;
    }

    public void setWater_resistance(Integer water_resistance) {
        this.water_resistance = water_resistance;
    }



    public String getBand_content() {
        return band_content;
    }

    public void setBand_content(String band_content) {
        this.band_content = band_content;
    }



    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }


}

